package org.reach24.Reach24Admin.dtos;

public class Services {

	private String system;
	private String subSystem;
	private String service;
	private String job;
	private String defect;
	private String location;
	private String assembly;
	private String component;
	private String position;
	private String workAccomplished;
	private String reasonforRepair;

	public String getSystem() {
		return system;
	}

	public void setSystem(String system) {
		this.system = system;
	}

	public String getSubSystem() {
		return subSystem;
	}

	public void setSubSystem(String subSystem) {
		this.subSystem = subSystem;
	}

	public String getService() {
		return service;
	}

	public void setService(String service) {
		this.service = service;
	}

	public String getJob() {
		return job;
	}

	public void setJob(String job) {
		this.job = job;
	}

	public String getDefect() {
		return defect;
	}

	public void setDefect(String defect) {
		this.defect = defect;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getAssembly() {
		return assembly;
	}

	public void setAssembly(String assembly) {
		this.assembly = assembly;
	}

	public String getComponent() {
		return component;
	}

	public void setComponent(String component) {
		this.component = component;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getWorkAccomplished() {
		return workAccomplished;
	}

	public void setWorkAccomplished(String workAccomplished) {
		this.workAccomplished = workAccomplished;
	}

	public String getReasonforRepair() {
		return reasonforRepair;
	}

	public void setReasonforRepair(String reasonforRepair) {
		this.reasonforRepair = reasonforRepair;
	}
}
